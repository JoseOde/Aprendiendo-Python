#Operadores Aritmeticos

print(3 + 4) #Suma
print(3 - 4) #Resta
print(3 * 4) #Multiplicacion
print(3 / 4) #Division
print(10 % 3) #Resto de una division
print(10 // 3) #El entero de una division
print(2 ** 3) #Potenciacion


#Operadores Comparativos

print(3 > 4) #Mayor que
print(3 < 4) #Menor que
print(3 >= 4) #Mayor o igual que
print(3 >= 4) #Menor o igual que
print(3 == 4) #Igual que
print(3 != 4) #No es igual que

#Operadore Logicos

print(4 > 3 and 3 < 5)
print(4 > 3 or 8 < 5)
print (not(3 > 4))